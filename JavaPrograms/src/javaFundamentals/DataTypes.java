package javaFundamentals;

public class DataTypes {
    public static void main(String[] args) {
        //* Data Types in Java is basically 2 types
        //* One is Primitive Datatype
        //* Another is Non-Primitive Datatype

        //* Primitives
        //* Integer Types - byte, short, int, long
        byte b = 127; // 1 byte
        short s = 3589; // 2 bytes
        int i = 258795; // 4 bytes
        long l = 2155544L; // 8 bytes

        //* Floating Points Types - float, double
        float f = 3.15f; // 4 bytes
        double d = 253.254258555; // 8 bytes

        //* Character Type - char
        char c = 'a'; // 2 bytes, because in java char supports Unicode's

        //* Boolean Type - boolean
        boolean bool = true; // basically 1 byte, but sometimes it's depends on JVM

        //* Non - Primitives
        //* Array, String, Class, Interface
        //* Talk about later

    }
}
